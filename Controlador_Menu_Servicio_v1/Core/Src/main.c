#define NO_PULSADO 1
#define PULSADO 0
#define BOTON_AZUL 1
#define BOTON_NEGRO 2

enum estados{Menu_Inicio, Menu_Tem_Hum, Menu_Led, Final_Menu};

uint8_t boton_azul_anterior;
uint8_t boton_azul_actual;
uint8_t menu_actual;

uint8_t readButtons(){

	// Codifica los botones que estan pulsados y devuelve el codigo de pulsacion de todos.
	uint8_t tipo_boton = 0;
	uint8_t boton_azul_actual = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13);

	if (boton_azul_anterior == NO_PULSADO && boton_azul_actual == PULSADO){
		tipo_boton = BOTON_AZUL;
		boton_azul_anterior = PULSADO;
	}
	boton_azul_anterior = boton_azul_actual;

	return tipo_boton;
}

uint8_t getTemp(){
	return 31;
}

uint8_t getHum(){
	return 50;
}

void menuInicio(){

	LCD_Clear();

	LCD_Set_Cursor(0, 0);
	LCD_Write_String("HOLA A TODOS!");

	LCD_Set_Cursor(1, 0);
	LCD_Write_String("Plsa azul xa sig.");

	while(readButtons() != BOTON_AZUL){
		;; // Espera pulsacion
	}
	menu_actual = (menu_actual + 1) % Final_Menu;
}

void mostrarTemHum(){

	LCD_Clear();

	LCD_Set_Cursor(0, 0);
	LCD_Write_String("Temp=");
	LCD_Set_Cursor(0, 9);
    LCD_Write_String("Hum= ");

    LCD_Set_Cursor(1, 0);
    LCD_Write_String("Plsa azul xa sig.");

	while(readButtons() != BOTON_AZUL){
		;; // Espera pulsacion
	}
	menu_actual = (menu_actual + 1) % Final_Menu;
}

void menuEncendidoLed(){

	LCD_Clear();
	LCD_Set_Cursor(0, 0);
	LCD_Write_String("Encendido LED");
	LCD_Set_Cursor(1, 0);
	LCD_Write_String("Plsa azul xa sig.");

	while(readButtons() != BOTON_AZUL){
		;; // hago cosas
		HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);
		HAL_Delay(100);
	}

	menu_actual = (menu_actual + 1) % Final_Menu;
}


int main(void)
{

  HAL_Init();

  SystemClock_Config();

  MX_GPIO_Init();
  MX_I2C1_Init();
  LCD_Init();

  boton_azul_anterior = 0;
  boton_azul_actual = 0;
  menu_actual = 0;

  while (1)
  {
	  switch (menu_actual){

	  	  case Menu_Inicio:
	  		  menuInicio();
	  		  break;

	  	  case Menu_Tem_Hum:
	  		  mostrarTemHum();
	  		  break;

	  	  case Menu_Led:
	  		  menuEncendidoLed();
	  		  break;

	  	  default:
	  		  menu_actual = 0;
	  		  break;
	  };

  }
}

